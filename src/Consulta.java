    public void removeSelectedRows(JTable table){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int[] rows = table.getSelectedRows();
        for(int i=0;i<rows.length;i++){
          model.removeRow(rows[i]-i);
        }
    }
